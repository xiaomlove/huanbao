    {
        $this->validate($request, [
            'email' => 'required|email|unique:users',
            'password' => 'required|min:6|max:20|confirmed',
        ]);
        $user = User::create([
            'email' => $request->email,
            'password' => bcrypt($request->password),
        ]);
        
        return redirect()->route('user.index');
    }

